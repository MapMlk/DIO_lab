result = 0

## Operadores Aritméticos
###Adição
result = 2+3
print("2 + 3 =", result)

###Subtração
result = (9-7)
print("9 - 7 =", result)

###Multiplicação
result = 3*5
print("3 x 5 =", result)

###Divisão
####Divisão normal
result = 7/2
print("Decimal", end=" ")
print("7 / 2 =", result)
####Divisão inteira
result = 7//2
print("Inteira", end=" ")
print("7 / 2 =", result)
####Modulo
result = 7%2
print("Modulo", end=" ")
print("7 % 2 =", result)

###Exponenciação
result = 2**3
print("2 ^ 3 =", result)
###Raiz quadrada
result = 9**(1/2)
print("Raiz de 9 =", result)

